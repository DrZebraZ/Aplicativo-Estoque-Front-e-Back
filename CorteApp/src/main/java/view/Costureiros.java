    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String url = "http://"+host+"/costureiro/addCostureiro";
        JSONObject json = new JSONObject();
        try {
            json.put("nome", jTextField1.getText());
            // Make the HTTP POST request and print the response body to the console
            String responseBody = ConnectionFactory.postJson(url, json);
            JOptionPane.showMessageDialog(null, responseBody);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, Arrays.toString(e.getStackTrace()));
        }        // TODO add your handling code here:
        alimentaTabela();        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String url = "http://"+host+"/costureiro/delCostureiro";
        JSONObject json = new JSONObject();
        try {
            json.put("id_costureiro", selectedDelete);
            // Make the HTTP POST request and print the response body to the console
            String responseBody = ConnectionFactory.deleteJson(url, json);
            JOptionPane.showMessageDialog(null, responseBody);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, Arrays.toString(e.getStackTrace()));
        }        // TODO add your handling code here:
        alimentaTabela();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        String selectedNome = (String) jComboBox1.getSelectedItem();
        if (selectedNome != null){
            Object selected = null;
            for (Object key : referenceMap.keySet()) {
                Object[] reference = (Object[]) key;
                String nome = (String) reference[1];
                if (nome.equals(selectedNome)) {
                    selected = key;
                    Object[] objeto = (Object[]) selected;
                    selectedDelete = (Integer) objeto[0];
                    break;
                }
            }
        }   
    }//GEN-LAST:event_jComboBox1ItemStateChanged


