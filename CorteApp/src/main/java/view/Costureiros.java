    public void alimentaTabela(){
        jComboBox1.setSelectedItem(null);
        referenceMap = new LinkedHashMap<>();
        jComboBox1.removeAllItems();
        String url = "http://"+this.host+"/costureiro/listarCostureiro";
        var result = ConnectionFactory.simpleGet(url);
        JSONArray jsonArray = new JSONArray(result);
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);     
        for (int i = 0; i < jsonArray.length(); i++) {
            JSONObject obj = jsonArray.getJSONObject(i);
            int id = obj.getInt("id");
            String nome = obj.getString("nome");
            referenceMap.put(new Object[]{id, nome}, id);
            jComboBox1.addItem(nome);
            modelo.addRow(new Object[]{nome, id});
        }
        jComboBox1.setSelectedItem(null);
        jTextField1.setText(null);
    }
