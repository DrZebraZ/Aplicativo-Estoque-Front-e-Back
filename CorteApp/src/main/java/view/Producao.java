    public void alimentaTabela(){
        jComboBox1.setSelectedItem(null);
        jComboBox2.setSelectedItem(null);
        jComboBox3.setSelectedItem(null);
        selectedAdd = null;
        selectedFinaliza = null;
        selectedCostureiro = null;
        referenceMap = new LinkedHashMap<>();
        costureiroMap = new LinkedHashMap<>();
        finalizaMap = new LinkedHashMap<>();
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
        String url1 = "http://"+this.host+"/producao/listaProducao";
        String url2 = "http://"+this.host+"/costureiro/listarCostureiro";
        String url3 = "http://"+this.host+"/produto/getListaProdutos";
        var result1 = ConnectionFactory.simpleGet(url1);
        var result2 = ConnectionFactory.simpleGet(url2);
        var result3 = ConnectionFactory.simpleGet(url3);
        JSONArray jsonArrayProducao = new JSONArray(result1);
        JSONArray jsonArrayCostureiro = new JSONArray(result2);
        JSONArray jsonArrayProduto = new JSONArray(result3);
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);     
        for (int i = 0; i < jsonArrayProducao.length(); i++) {
            JSONObject obj = jsonArrayProducao.getJSONObject(i);
            int id = obj.getInt("id_produto");
            String referencia = obj.getString("referencia");
            int quantia = obj.getInt("quantidade");
            Integer finalizado = 0;
            String finalizadosStr = obj.optString("finalizados");
            if (finalizadosStr != null && finalizadosStr != "") {
                finalizado = Integer.parseInt(finalizadosStr);
            }
            finalizaMap.put(new Object[]{id, referencia, quantia, finalizado}, id);
            jComboBox2.addItem(referencia);            
            modelo.addRow(new Object[]{referencia,quantia ,finalizado});
        }
        for (int i = 0; i < jsonArrayCostureiro.length(); i++) {
            JSONObject obj = jsonArrayCostureiro.getJSONObject(i);
            int id = obj.getInt("id");
            String nome = obj.getString("nome");
            costureiroMap.put(new Object[]{id, nome}, id);
            jComboBox3.addItem(nome);            
        }
        for (int i = 0; i < jsonArrayProduto.length(); i++) {
            JSONObject obj = jsonArrayProduto.getJSONObject(i);
            int id = obj.getInt("id");
            String referencia = obj.getString("referencia");
            referenceMap.put(new Object[]{id, referencia}, id);
            jComboBox1.addItem(referencia);
        }
        jComboBox1.setSelectedItem(null);
        jComboBox2.setSelectedItem(null);
        jComboBox3.setSelectedItem(null);
        jTextField1.setText(null);
        jTextField2.setText(null);
    }

