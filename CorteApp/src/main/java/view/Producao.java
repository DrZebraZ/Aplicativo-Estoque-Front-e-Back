    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String url = "http://"+host+"/producao/add";
        JSONObject json = new JSONObject();
        try {
            json.put("id_produto", selectedAdd[0]);
            json.put("quantia", jTextField1.getText().isEmpty() ? 0 : Integer.parseInt(jTextField1.getText()));
            // Make the HTTP POST request and print the response body to the console
            String responseBody = ConnectionFactory.postJson(url, json);
            JOptionPane.showMessageDialog(null, responseBody);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, Arrays.toString(e.getStackTrace()));
        }        // TODO add your handling code here:
        alimentaTabela();
    }//GEN-LAST:event_jButton1ActionPerformed

