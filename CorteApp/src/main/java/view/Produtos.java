    public void alimentaTabela(){
        jComboBox1.setSelectedItem(null);
        jComboBox2.setSelectedItem(null);
        selectedEdit = null;
        selectedDelete = null;
        referenceMap = new LinkedHashMap<>();
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        String url = "http://"+this.host+"/produto/getListaProdutos";
        var result = ConnectionFactory.simpleGet(url);
        JSONArray jsonArray = new JSONArray(result);
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);     
        for (int i = 0; i < jsonArray.length(); i++) {
            JSONObject obj = jsonArray.getJSONObject(i);
            int id = obj.getInt("id");
            String referencia = obj.getString("referencia");
            int estoque = obj.getInt("estoque");
            String descricao = obj.getString("descricao");
            referenceMap.put(new Object[]{referencia, descricao, estoque, id}, id);
            modelo.addRow(new Object[]{referencia,descricao ,estoque});
            jComboBox1.addItem(referencia);
            jComboBox2.addItem(referencia);
        }
        jComboBox1.setSelectedItem(null);
        jTextField1.setText(null);
        jTextField2.setText(null);
        jTextField3.setText(null);
        jTextField4.setText(null);
        jTextField5.setText(null);
        jTextField6.setText(null);
        jComboBox2.setSelectedItem(null);
    }

