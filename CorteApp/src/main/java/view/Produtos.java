    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String url = "http://"+host+"/produto/editaProduto";
        try {
            String referencia = jTextField4.getText();
            String descricao = (jTextField5.getText().isEmpty() ? " " : jTextField5.getText());
            var estoque = jTextField6.getText().isEmpty() ? 0 : Integer.parseInt(jTextField6.getText());
            JSONObject json = new JSONObject();
            json.put("id", this.selectedEdit[3]);
            json.put("referencia", referencia);
            json.put("descricao", descricao);
            json.put("estoque", estoque);
            // Make the HTTP POST request and print the response body to the console
            String responseBody = ConnectionFactory.putJson(url, json);
            JOptionPane.showMessageDialog(null, responseBody);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, Arrays.toString(e.getStackTrace()));
        }        // TODO add your handling code here:
        alimentaTabela();
// TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

